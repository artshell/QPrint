package com.qw.print.data.style;

/**
 * 字体拉伸类型
 * Created by qinwei on 2019-11-09 10:02
 * email: qinwei_it@163.com
 * @author qinwei
 */
public enum Stretch {
    /**
     * 不拉伸
     */
    NONE,
    /**
     * 纵向拉伸2倍
     */
    VERTICAL,
    /**
     * 横向拉伸2倍
     */
    HORIZONTAL,
    /**
     * 纵向横向一起拉伸2倍
     */
    BOTH
}
